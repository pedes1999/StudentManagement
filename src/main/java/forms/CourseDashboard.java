    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRowIndex = jTable1.getSelectedRow();
        Integer id = (Integer) jTable1.getValueAt(selectedRowIndex, 0);
        String name = crsName.getText().toLowerCase();
        Date startDate = datePicker1.getDate();
        LocalDate sDate = startDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        Date endDate = datePicker.getDate();
        LocalDate eDate = endDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        Integer profId = (Integer) prof.getValue();
        Optional<Professor> p = profService.findById(profId);
        if (p.isPresent()) {
            Course s = new Course(name, p.get(), sDate, eDate);
            courseService.updateCourse(id, s);
            addToTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please enter a valid professor id!", "Empty fields!!", 0);
        }
    }//GEN-LAST:event_editActionPerformed

    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        addToTable();
        String name = crsName.getText().toLowerCase();
        Integer profId = (Integer) prof.getValue();
        Optional<Professor> p = profService.findById(profId);
        Date startDate = datePicker1.getDate();
        LocalDate sDate = startDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        Date endDate = datePicker1.getDate();
        LocalDate eDate = endDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        if(p.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a valid professor id!", "Empty fields!!", 0);
        }
        
        if (name.trim().equals("") || profId.equals(0)) {
            JOptionPane.showMessageDialog(null, "Please enter all fields correctly", "Empty fields!!", 0);
        } else {
            Course c = new Course(name, p.get(), sDate, eDate);
            try {
            courseService.add(c);
            } catch (Exception e) {
                logger.warn("There is no Professor with that id " , e); 
            }
            addToTable();
        }

    }//GEN-LAST:event_addActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRowIndex = jTable1.getSelectedRow();
        Integer id = (Integer) jTable1.getValueAt(selectedRowIndex, 0);
        System.out.println(id);
        int result = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete course with id : " + id, "Delete Student", JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if (result == JOptionPane.YES_OPTION) {
            try {
                courseService.deleteById(id);
                JOptionPane.showMessageDialog(null, "Course with id : " + id + " deleted Successfully!!", "Success", 1);
                addToTable();
            } catch (Exception ignore) {
                addToTable();
            }

        }

    }//GEN-LAST:event_deleteActionPerformed

