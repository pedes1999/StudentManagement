    private void addToTable() {
        Object[] columnNames = {"Id", "name", "startDate", "EndDate", "professorId"};
        DefaultTableModel model = new DefaultTableModel(new Object[0][0], columnNames);
        List<Course> l = courseService.findAll();
        for (Course s : l) {
            Object[] o = new Object[5];
            o[0] = s.getCourseId();
            o[1] = s.getCourseName();
            o[2] = s.getStartDate();
            o[3] = s.getEndDate();
            o[4] = s.getCourseProfessor().getProfessorId();

            model.addRow(o);

        }
        jTable1.setRowHeight(30);
        jTable1.setModel(model);
    }

